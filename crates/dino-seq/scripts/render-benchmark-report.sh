#!/usr/bin/env bash
set -euo pipefail

jsonl="${1:-target/bench-results/dino_seq-gauntlet.jsonl}"
out_dir="${2:-docs/benchmarks/latest}"
fig_dir="${out_dir}/figures"
result_dir="$(dirname "${jsonl}")"
metadata="${result_dir}/dino_seq-gauntlet-metadata.md"
external_tsv="${result_dir}/external-tools.tsv"
external_parity_tsv="${result_dir}/external-parity.tsv"
raw_jsonl_out="${out_dir}/dino_seq-gauntlet.jsonl"
metadata_out="${out_dir}/metadata.md"
external_tsv_out="${out_dir}/external-tools.tsv"
external_parity_tsv_out="${out_dir}/external-parity.tsv"

if [[ ! -s "${jsonl}" ]]; then
  printf 'benchmark JSONL not found or empty: %s\n' "${jsonl}" >&2
  exit 1
fi
if ! command -v jq >/dev/null 2>&1; then
  printf 'jq is required to render benchmark reports\n' >&2
  exit 1
fi

write_sanitized_file() {
  local src="$1"
  local dst="$2"
  local tmp
  tmp="$(mktemp "${dst}.tmp.XXXXXX")"
  if [[ -n "${HOME:-}" ]]; then
    awk -v home="${HOME}" '{ gsub(home, "~"); print }' "${src}" > "${tmp}"
  else
    cp "${src}" "${tmp}"
  fi
  mv "${tmp}" "${dst}"
}

mkdir -p "${fig_dir}"

write_sanitized_file "${jsonl}" "${raw_jsonl_out}"
if [[ -f "${metadata}" ]]; then
  write_sanitized_file "${metadata}" "${metadata_out}"
fi
if [[ -f "${external_tsv}" ]]; then
  write_sanitized_file "${external_tsv}" "${external_tsv_out}"
fi
if [[ -f "${external_parity_tsv}" ]]; then
  write_sanitized_file "${external_parity_tsv}" "${external_parity_tsv_out}"
fi

rows="$(mktemp)"
pack_rows="$(mktemp)"
external_rows="$(mktemp)"
trap 'rm -f "${rows}" "${pack_rows}" "${external_rows}"' EXIT

jq -r '
  . as $run
  | .measurements[]
  | select(.name == "file-auto" or .name == "file-paired-auto")
  | [
      (
        $run.source
        | if contains("+") then
            split("+") | map(split("/")[-1]) | join("+")
          else
            split("/")[-1]
          end
      ),
      .name,
      .records,
      .bases,
      .best_ns,
      ((.records * 1000000000 / .best_ns) | floor),
      ((.bases * 1000000000 / .best_ns) | floor),
      .checksum
    ]
  | @tsv
' "${jsonl}" > "${rows}"

jq -r '
  . as $run
  | .measurements[]
  | select(.name | contains("pack-seq-qual"))
  | [
      (
        $run.source
        | if contains("+") then
            split("+") | map(split("/")[-1]) | join("+")
          else
            split("/")[-1]
          end
      ),
      .name,
      .records,
      .bases,
      .best_ns,
      ((.records * 1000000000 / .best_ns) | floor),
      ((.bases * 1000000000 / .best_ns) | floor),
      .checksum
    ]
  | @tsv
' "${jsonl}" > "${pack_rows}"

if [[ -f "${external_tsv_out}" ]]; then
  awk -F '\t' 'NR > 1 && $3 == "0" && $4 != "" && $4 != "NA" {
    print $1 "\t" $2 "\t" $4
  }' "${external_tsv_out}" > "${external_rows}"
fi

summary="${out_dir}/summary.md"
auto_svg="${fig_dir}/auto-bases-throughput.svg"
pack_svg="${fig_dir}/pack-bases-throughput.svg"
external_svg="${fig_dir}/external-tool-wallclock.svg"

{
  printf '# Dino Seq Benchmark Snapshot\n\n'
  printf 'Raw measurements: [`dino_seq-gauntlet.jsonl`](dino_seq-gauntlet.jsonl)\n\n'
  printf 'Source JSONL: `%s`\n\n' "${jsonl}"
  printf 'This report is generated by `scripts/render-benchmark-report.sh` from benchmark JSONL.\n'
  printf 'Rows may include deterministic synthetic fixtures and optional local corpus inputs. Treat this as machine-local evidence; use recorded metadata, exact commands, and real datasets before making public performance claims.\n\n'
  printf '| input | row | records | bases | best ms | records/s | bases/s | checksum |\n'
  printf '| --- | --- | ---: | ---: | ---: | ---: | ---: | ---: |\n'
  awk -F '\t' '{
    printf "| `%s` | `%s` | %s | %s | %.3f | %s | %s | `%s` |\n",
      $1, $2, $3, $4, $5 / 1000000.0, $6, $7, $8
  }' "${rows}"
  printf '\n'
  printf '## Pack And Side-Channel Rows\n\n'
  printf 'These rows include base packing and quality summary work. They should not be compared directly with parse-only rows.\n\n'
  printf '| input | row | records | bases | best ms | records/s | bases/s | checksum |\n'
  printf '| --- | --- | ---: | ---: | ---: | ---: | ---: | ---: |\n'
  awk -F '\t' '{
    printf "| `%s` | `%s` | %s | %s | %.3f | %s | %s | `%s` |\n",
      $1, $2, $3, $4, $5 / 1000000.0, $6, $7, $8
  }' "${pack_rows}"
  printf '\n'
  if [[ -f "${external_tsv_out}" ]]; then
    printf '## External Tool Timings\n\n'
    printf 'External rows are wall-clock timings for comparable command-line work where the tool is installed locally.\n\n'
    if [[ -f "${external_parity_tsv_out}" ]]; then
      printf 'Parity sidecar: [`external-parity.tsv`](external-parity.tsv)\n\n'
    fi
    printf '| label | tool | status | elapsed s |\n'
    printf '| --- | --- | ---: | ---: |\n'
    awk -F '\t' 'NR > 1 {
      elapsed = $4
      if (elapsed == "") {
        elapsed = "NA"
      }
      printf "| `%s` | `%s` | `%s` | `%s` |\n", $1, $2, $3, elapsed
    }' "${external_tsv_out}"
    printf '\n'
  fi
  if [[ -f "${metadata_out}" ]]; then
    printf 'Metadata: [`metadata.md`](metadata.md)\n\n'
  fi
  if [[ -f "${out_dir}/input-manifest.tsv" ]]; then
    printf 'Input manifest: [`input-manifest.tsv`](input-manifest.tsv)\n\n'
  fi
  printf 'Figures:\n\n'
  printf -- '- [`figures/auto-bases-throughput.svg`](figures/auto-bases-throughput.svg)\n'
  printf -- '- [`figures/pack-bases-throughput.svg`](figures/pack-bases-throughput.svg)\n'
  if [[ -s "${external_rows}" ]]; then
    printf -- '- [`figures/external-tool-wallclock.svg`](figures/external-tool-wallclock.svg)\n'
  fi
} > "${summary}"

render_bases_svg() {
  local input_rows="$1"
  local title="$2"
  local subtitle="$3"
  local output_svg="$4"

  awk -F '\t' -v title="${title}" -v subtitle="${subtitle}" '
    {
      input[NR] = $1
      row[NR] = $2
      bases_s[NR] = $7 + 0
      if (bases_s[NR] > max) {
        max = bases_s[NR]
      }
    }
    END {
      n = NR
      width = 1120
      left = 310
      bar_max = 650
      row_h = 30
      top = 54
      height = top + n * row_h + 52
      print "<svg xmlns=\"http://www.w3.org/2000/svg\" width=\"" width "\" height=\"" height "\" viewBox=\"0 0 " width " " height "\">"
      print "<rect width=\"100%\" height=\"100%\" fill=\"white\"/>"
      print "<text x=\"24\" y=\"28\" font-family=\"Arial, sans-serif\" font-size=\"18\" font-weight=\"700\">" title "</text>"
      print "<text x=\"24\" y=\"46\" font-family=\"Arial, sans-serif\" font-size=\"12\" fill=\"#555\">" subtitle "</text>"
      for (i = 1; i <= n; i++) {
        y = top + (i - 1) * row_h
        bar = max > 0 ? int((bases_s[i] / max) * bar_max) : 0
        label = input[i] " / " row[i]
        printf "<text x=\"24\" y=\"%d\" font-family=\"Arial, sans-serif\" font-size=\"12\" fill=\"#222\">%s</text>\n", y + 17, label
        printf "<rect x=\"%d\" y=\"%d\" width=\"%d\" height=\"18\" fill=\"#1f766f\"/>\n", left, y + 4, bar
        printf "<text x=\"%d\" y=\"%d\" font-family=\"Arial, sans-serif\" font-size=\"12\" fill=\"#222\">%.2f Gbases/s</text>\n", left + bar + 8, y + 17, bases_s[i] / 1000000000.0
      }
      print "</svg>"
    }
  ' "${input_rows}" > "${output_svg}"
}

render_external_svg() {
  local input_rows="$1"
  local output_svg="$2"
  [[ -s "${input_rows}" ]] || return 0

  awk -F '\t' '
    {
      label[NR] = $1
      tool[NR] = $2
      elapsed[NR] = $3 + 0
      if (elapsed[NR] > max) {
        max = elapsed[NR]
      }
    }
    END {
      n = NR
      width = 980
      left = 290
      bar_max = 560
      row_h = 30
      top = 54
      height = top + n * row_h + 52
      print "<svg xmlns=\"http://www.w3.org/2000/svg\" width=\"" width "\" height=\"" height "\" viewBox=\"0 0 " width " " height "\">"
      print "<rect width=\"100%\" height=\"100%\" fill=\"white\"/>"
      print "<text x=\"24\" y=\"28\" font-family=\"Arial, sans-serif\" font-size=\"18\" font-weight=\"700\">External tool wall-clock timings</text>"
      print "<text x=\"24\" y=\"46\" font-family=\"Arial, sans-serif\" font-size=\"12\" fill=\"#555\">seconds; only installed tools with successful exit status are shown</text>"
      for (i = 1; i <= n; i++) {
        y = top + (i - 1) * row_h
        bar = max > 0 ? int((elapsed[i] / max) * bar_max) : 0
        text = label[i]
        printf "<text x=\"24\" y=\"%d\" font-family=\"Arial, sans-serif\" font-size=\"12\" fill=\"#222\">%s</text>\n", y + 17, text
        printf "<rect x=\"%d\" y=\"%d\" width=\"%d\" height=\"18\" fill=\"#7a4a1d\"/>\n", left, y + 4, bar
        printf "<text x=\"%d\" y=\"%d\" font-family=\"Arial, sans-serif\" font-size=\"12\" fill=\"#222\">%.2f s</text>\n", left + bar + 8, y + 17, elapsed[i]
      }
      print "</svg>"
    }
  ' "${input_rows}" > "${output_svg}"
}

render_bases_svg \
  "${rows}" \
  "Dino Seq auto parse throughput" \
  "bases/s; parse-only auto opener rows generated from benchmark JSONL" \
  "${auto_svg}"

render_bases_svg \
  "${pack_rows}" \
  "Dino Seq pack and side-channel throughput" \
  "bases/s; rows include base packing and quality summary work" \
  "${pack_svg}"

render_external_svg "${external_rows}" "${external_svg}"

printf 'wrote %s\n' "${summary}"
printf 'wrote %s\n' "${raw_jsonl_out}"
printf 'wrote %s\n' "${auto_svg}"
printf 'wrote %s\n' "${pack_svg}"
if [[ -s "${external_rows}" ]]; then
  printf 'wrote %s\n' "${external_svg}"
fi
